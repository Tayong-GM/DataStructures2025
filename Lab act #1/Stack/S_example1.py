stack = []#popping out an element

# Push elements onto the stack (add to the top)
stack.append(10)
stack.append(20)
stack.append(30)

# Pop an element from the stack (remove from the top)
popped = stack.pop()

# Print the stack and the popped element
print(stack)  # Output: [10, 20]
print("Popped:", popped)  # Output: Popped: 30
